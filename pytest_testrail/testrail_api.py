import requests
import simplejson as json


class APIClient:
    def __init__(self, base_url):
        self.user = ''
        self.password = ''
        if not base_url.endswith('/'):
            base_url += '/'
        self.__url = base_url + 'index.php?/api/v2/'
        self.headers = {'Content-Type': 'application/json'}

    #
    # Send Get
    #
    # Issues a GET request (read) against the API and returns the result
    # (as Python dict).
    #
    # Arguments:
    #
    # uri                 The API method to call including parameters
    #                     (e.g. get_case/1)
    #
    def send_get(self, uri, cert_check):
        url = self.__url + uri
        r = requests.get(
            url,
            auth=(self.user, self.password),
            headers=self.headers,
            verify=cert_check
        )
        return r.json()

    #
    # Send POST
    #
    # Issues a POST request (write) against the API and returns the result
    # (as Python dict).
    #
    # Arguments:
    #
    # uri                 The API method to call including parameters
    #                     (e.g. add_case/1)
    # data                The data to submit as part of the request (as
    #                     Python dict, strings must be UTF-8 encoded)
    #
    def send_post(self, uri, data, cert_check):
        url = self.__url + uri
        r = requests.post(
            url,
            auth=(self.user, self.password),
            headers=self.headers,
            data=json.dumps(data),
            verify=cert_check
        )
        return r.json()

    @staticmethod
    def get_error(json_response):
        """ Extract error contained in a API response.
            If no error occured, return None

            :param json_response: json response of request
            :return: String of the error
        """
        if 'error' in json_response and json_response['error']:
            return json_response['error']
